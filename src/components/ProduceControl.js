class ProduceControl extends React.Component {

  constructor(props) {
    super(props);
    this.state = {
      produceSelectionVisibleOnPage: 0
    };
  }

  handleClickDecrease = () => {
    this.setState(prevState => ({
      produceSelectionVisibleOnPage: prevState.produceSelectionVisibleOnPage - 1
    }));
  }

  handleClickIncrease = () => {
    this.setState(prevState => ({
      produceSelectionVisibleOnPage: prevState.produceSelectionVisibleOnPage + 1
    }));
  }

  render() {
    let currentlyVisibleState = null;
    let previousMonthButton = null;
    let nextMonthButton = null;

    const previousMonth = availableProduce[this.state.produceSelectionVisibleOnPage - 1]
    const selectedMonth = availableProduce[this.state.produceSelectionVisibleOnPage]
    const nextMonth = availableProduce[this.state.produceSelectionVisibleOnPage + 1]

    const month = <ProduceList month={selectedMonth.month}
      names={selectedMonth.selection} />

    if (this.state.produceSelectionVisibleOnPage < 1) {
      currentlyVisibleState = month
      nextMonthButton = <button onClick={this.handleClickIncrease}>See {nextMonth.month}'s produce selection &#8594;</button>
    } else if (this.state.produceSelectionVisibleOnPage > 10) {
      currentlyVisibleState = month
      previousMonthButton = <button onClick={this.handleClickDecrease}>&#8592; See {previousMonth.month}'s produce selection</button>
    } else {
      currentlyVisibleState = month
      previousMonthButton = <button onClick={this.handleClickDecrease}>&#8592; See {previousMonth.month}'s produce selection</button>
      nextMonthButton = <button onClick={this.handleClickIncrease}>See {nextMonth.month}'s produce selection &#8594;</button>
    }

    return (
      <>
        {currentlyVisibleState}
        {previousMonthButton}
        {nextMonthButton}
      </>
    );
  }
}

export default ProduceControl;